                    <h1>O GCC-SD</h1>
                    <p>O <a href="http://gccsd.com.br" title="Blog do GCC-SD"><strong>Grupo de Compartilhamento do Conhecimento Santos Dumont</strong></a> é um grupo formado principalmente por usuários de software livre e que tem como objetivo a troca de conhecimento e informações entre os membros.</p>
					<h2>Como o Grupo Surgiu?</h2>

					<p>O Grupo de Compartilhamento do Conhecimento Santos Dumont surgiu em 2007 para a preparação do primeiro Flisol em Santos. Na época o grupo ainda não tinha esse nome e os únicos membros que participaram daquele Flisol e que continuaram participando dos encontros do grupo foram o <a href="http://infog.casoft.info" title="Blog do InFog">InFog</a> e o <a href="http://kretcheu.com.br" title="Kretcheu Vídeo Blog">Kretcheu</a>.</p>

					<p>E foi durante 2007 que o grupo foi batizado de <strong>GCC Santos Dumont</strong>. Antes disso algumas outras opções de nome foram consideradas, já que nós não queriamos seguir o velho padrão GUSLXX (Grupo de Usuários de Software Livre de “Algum Lugar”). Um dos nomes sugeridos foi Marruá, que na cultura nordestina é usado para definir os cavalos selvagens que nunca foram domados, por isso não livres. É um ótimo nome, com um significado muito legal para o grupo, mas a opinião dos membros foi de que le não soava bem.</p>

					<p>Já no ano de 2008 para a realização do segundo Flisol contamos com a participação de mais pessoas, como é o caso do Sprity e do <a href="http://blog.thiagofazzio.com.br" title="Blog do Guariní">Guariní</a>, que conhecemos durante uma palestra de software livre realizada pelo Kretcheu na Unimonte.</p>

					<p>Durante o ano de 2008, o grupo teve a participação de apenas três membros, Guariní, InFog e Kretcheu. Tudo bem que lá de vez em quando aparecia algum rosto novo, mas acho que isso aconteceu uma ou duas vezes.</p>

					<p>No Flisol de 2009 novos membros se juntaram ao grupo, um deles é o Phoenix, e agora o compartilhamento de informações e a disseminação do software livre está ganhando força na região de Santos!</p>

					<h2>Sobre o nome...</h2>

					<p>O nome GCC faz alusão ao famoso <strong>Gnu C Compiler</strong>, o compilador do projeto Gnu, e a homenagem à Santos Dumont foi feita por que ele não patenteava suas invenções, acreditando que o progresso deve acontecer à toda a humanidade e não apenas à algumas pessoas.</p>

