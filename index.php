<h3>Você sabe o que é Flisol?</h3>
<p>O <strong>Festival Latino Americano de Instalação de Software Livre</strong> (<strong>Flisol</strong>) é um evento regional, realizado anualmente, e que ocorre de forma simultânea em diversas cidades da América Latina.</p>
<p>O Flisol é um evento descentralizado, onde cada local escolhido realiza seu festival simultaneamente com os outros pontos espalhados pela América Latina.</p>
<p>O evento é livre, e nesse dia há a instalação de <strong>Software Livre</strong>, como distribuições de <strong>Gnu/Linux</strong>, <strong>sistemas BSD</strong>, e aplicativos livres para Windows em geral.</p>
<h4>Objetivo do evento</h4>
<p>O objetivo do Flisol é promover o uso de software livre em geral, além de criar interações entre usuários e desenvolvedores.</p>
